#include <string.h>
#include <unistd.h>

int main()
{
	char buf[512];
	char ecc[16] = {0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF};
	memset(buf, 0xFF, 512);
	while(read(0,buf,512))
	{
		write(1,buf,512);
		memset(buf, 0xFF, 512);
		write(1,ecc,16);
	}
	return 0;
}
